package com.tonmx.inspection;

class OwnerListItem {
    private int id;
    private String username;
    private String ownform;
    private String owntime;
    private int status;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getOwnform() {
        return ownform;
    }

    public void setOwnform(String ownform) {
        this.ownform = ownform;
    }

    public String getOwntime() {
        return owntime;
    }

    public void setOwntime(String owntime) {
        this.owntime = owntime;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
